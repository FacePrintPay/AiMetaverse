#!/data/data/com.termux/files/usr/bin/bash
set -euo pipefail

TARGET="$1"
OUT="$TARGET/receipt.json"

echo '{ "anchored_at": "'"$(date -u +"%Y-%m-%dT%H:%M:%SZ")"'", "artifacts": {' > "$OUT"

first=1
while IFS= read -r -d '' f; do
  hash=$(sha256sum "$f" | cut -d' ' -f1)
  name=$(basename "$f")
  [ $first -eq 0 ] && echo "," >> "$OUT"
  first=0
  echo "  \"$name\": \"$hash\"" >> "$OUT"
done < <(find "$TARGET" -type f -print0)

echo "} }" >> "$OUT"
EOT
